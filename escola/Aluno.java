package escola;

public class Aluno {
    private String name;
    private int idade;

    
    public String getName() {
        return name;
    }
    public void setName(String NewName) {
        this.name = NewName;
    }
    public int getIdade() {
        return idade;
    }
    public void setIdade(int newIdade) {
        this.idade = newIdade;
    }    
    
    
}
